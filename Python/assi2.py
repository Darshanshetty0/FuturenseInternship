# 1. Assign the string "Hello, World!" to a variable 'greeting'. Use the '.lower()' method to convert it to lowercase and print the result.
greeting = "Hello, World!"
print("Lowercase greeting:", greeting.lower())

# 2. Given the string "python programming", use the '.capitalize()' method to capitalize the first letter of the string and print the result.
python_string = "python programming"
print("Capitalized string:", python_string.capitalize())

# 3. Create a string variable 'text' with the value " Learn Python! ". Use the '.strip()' method to remove the leading and trailing whitespace and print the result.
text = " Learn Python! "
print("Stripped text:", text.strip())

# 4. Given the string "AIandML", use the '.find()' method to find the index of the substring "AI" and print the result.
ai_ml_string = "AIandML"
print("Index of 'AI':", ai_ml_string.find("AI"))

# 5. Assign the string "Python is fun" to a variable 'phrase'. Use the '.replace()' method to replace "fun" with "awesome" and print the result.
phrase = "Python is fun"
print("Replaced phrase:", phrase.replace("fun", "awesome"))

# 6. Given the string "Hello, World!", use the '.split()' method to split the string into a list of words and print the result.
hello_world = "Hello, World!"
print("Split string:", hello_world.split())

# 7. Create a string variable 'sentence' with the value "this is a test". Use the '.title()' method to convert it to title case and print the result.
sentence = "this is a test"
print("Title case sentence:", sentence.title())

# 8. Assign the string "apple,banana,cherry" to a variable 'fruits'. Use the '.split()' method with a comma as the separator to split the string into a list and print the result.
fruits = "apple,banana,cherry"
print("Split fruits:", fruits.split(','))

# 9. Given the string "Python", use the '.center()' method to center it within a width of 20 characters, padding with the character '*', and print the result.
python_centered = "Python".center(20, '*')
print("Centered string:", python_centered)

# 10. Create a string variable 'quote' with the value "To be or not to be". Use the '.count()' method to count the occurrences of the substring "be" and print the result.
quote = "To be or not to be"
print("Count of 'be':", quote.count("be"))
